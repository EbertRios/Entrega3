package com.mycompany.escuelaproyecto.Models;


class Asignacion {
    private int id;
    private String fecha;

    
    public Asignacion(int id, String fecha) {
        this.id = id;
        this.fecha = fecha;
    }

    // Getters y setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
}
